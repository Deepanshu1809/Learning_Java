class Larger {
  public static void main(String[] args) {

      int a=10,b=15,c=20;
      
      if(a>b && a>c) {
          System.out.println("a is Greatest");
      }else if(b>a && b>c) {
          System.out.println("b is Greatest");
      }else if(c>a && c>b) {
          System.out.println("c is Greatest");
      }
  } 
}